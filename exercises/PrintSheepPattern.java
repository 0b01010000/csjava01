package exercises;

public class PrintSheepPattern {
    public static void main(String[] args) {
         /*                     '__'
                                (oo)
                        /========//
                       / || @@ ||
                      o  ||----||
                         VV    VV
                         ''    ''
        */
        System.out.println("          '__'");                    
        System.out.println("          (oo)");
        System.out.println("  /========// ");
        System.out.println(" / || @@ ||   ");
        System.out.println("*  ||----||   ");
        System.out.println("   VV    VV   ");
        System.out.println("   ''    ''");
                    
    }
}
